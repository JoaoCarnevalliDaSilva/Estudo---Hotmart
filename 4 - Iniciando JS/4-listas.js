/* Criando uma lista que eu ja determino os itens da lista*/
var lista = ['Matheus', 'Lucas', 'Jose', 150];

lista.indexOf('Jose'); /* Devolve a posicao do elemento procurado */

lista.push('Carlos'); /* Adicionando itens a lista */

lista.shift(); /* Remove o primeiro item da lista */

lista.pop(); /* Remove o ultimo item da lista */

lista.join(';'); /* Separa os itens de acordo com o caracter em () */